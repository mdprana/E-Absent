    private void autoid(){
        try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dblogin","root","");
                
                // Menggunakan Statement untuk mengeksekusi perintah SQL
                String sql = "SELECT * FROM mhs ORDER BY NIM DESC";
                Statement stmt = con.createStatement();
                
                ResultSet rs = stmt.executeQuery(sql);
                
                if (rs.next()) {
                    String NIM = rs.getString("NIM");
                    String id = NIM.substring(0);
                    txid.setText(id);
                    
                } else {
                    // Jika tidak ada data di tabel 'mhs', atur nilai default
                    txid.setText("error");
                }
            } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ada Error");
        }
    }   

