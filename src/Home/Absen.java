    public void show_data(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("NIM");
        tabel.addColumn("Nama");
        tabel.addColumn("Matkul");
        tabel.addColumn("Pertemuan");
        tabel.addColumn("Ket");
        try {
            java.sql.Connection con = (java.sql.Connection)Home.Koneksi.getKoneksi();
            String sql = "SELECT mhs.NIM, mhs.Nama, matkul.nama_matkul, pertemuan.tgl, absen.ket FROM absen LEFT JOIN mhs ON absen.NIM = mhs.NIM LEFT JOIN matkul ON absen.nama_matkul = matkul.nama_matkul LEFT JOIN pertemuan ON absen.id_pertemuan = pertemuan.id_pertemuan";
            java.sql.PreparedStatement stm = con.prepareStatement(sql);
            ResultSet rs =stm.executeQuery(sql);
            while (rs.next()) {
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5)});
                }
            jTable1.setModel(tabel);
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Error bro");
                System.out.println(e.getMessage());
            }
    }
    
